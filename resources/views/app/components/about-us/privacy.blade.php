        <h2>Kebijakan Privasi</h2>
        <p>TechTalk menghargai privasi Anda dan berkomitmen untuk melindungi informasi pribadi yang Anda berikan kepada
            kami. Kebijakan Privasi ini menjelaskan bagaimana kami mengumpulkan, menggunakan, dan melindungi data Anda
            saat menggunakan layanan kami.</p>

        <h3>1. Informasi yang Kami Kumpulkan</h3>
        <p>Kami dapat mengumpulkan informasi berikut saat Anda menggunakan TechTalk:</p>
        <ul>
            <li><strong>Informasi Akun:</strong> Nama, alamat email, dan detail lain yang Anda berikan saat mendaftar.
            </li>
            <li><strong>Data Aktivitas:</strong> Diskusi, komentar, dan konten yang Anda unggah.</li>
            <li><strong>Informasi Teknis:</strong> Alamat IP, jenis perangkat, dan data log lainnya untuk meningkatkan
                layanan kami.</li>
        </ul>

        <h3>2. Cara Kami Menggunakan Informasi</h3>
        <p>Informasi yang kami kumpulkan digunakan untuk:</p>
        <ul>
            <li>Memberikan akses ke fitur dan layanan TechTalk.</li>
            <li>Meningkatkan pengalaman pengguna melalui analisis dan umpan balik.</li>
            <li>Menjaga keamanan komunitas dan mencegah aktivitas yang melanggar hukum.</li>
            <li>Mengirimkan pemberitahuan terkait akun Anda atau fitur baru yang relevan.</li>
        </ul>

        <h3>3. Pengungkapan Informasi kepada Pihak Ketiga</h3>
        <p>Kami tidak menjual atau menyewakan informasi pribadi Anda kepada pihak ketiga. Namun, kami dapat membagikan
            informasi dengan:</p>
        <ul>
            <li><strong>Mitra layanan:</strong> Untuk menyediakan dan meningkatkan layanan, seperti penyimpanan data
                atau analitik.</li>
            <li><strong>Penegak hukum:</strong> Jika diwajibkan oleh hukum atau untuk melindungi hak kami dan pengguna
                lain.</li>
        </ul>

        <h3>4. Keamanan Informasi</h3>
        <p>Kami menggunakan langkah-langkah teknis dan organisasi untuk melindungi informasi pribadi Anda dari akses
            yang tidak sah, kehilangan, atau kerusakan. Namun, tidak ada sistem yang sepenuhnya aman, jadi kami
            mendorong Anda untuk menjaga kerahasiaan kredensial akun Anda.</p>

        <h3>5. Hak Pengguna</h3>
        <p>Anda memiliki hak untuk:</p>
        <ul>
            <li>Mengakses dan memperbarui informasi pribadi Anda melalui pengaturan akun.</li>
            <li>Meminta penghapusan akun dan data Anda dari sistem kami.</li>
            <li>Menolak pengumpulan data tertentu dengan menyesuaikan preferensi cookie atau pengaturan privasi.</li>
        </ul>

        <h3>6. Cookie dan Teknologi Pelacakan</h3>
        <p>Kami menggunakan cookie dan teknologi pelacakan lainnya untuk:</p>
        <ul>
            <li>Meningkatkan performa situs.</li>
            <li>Mengingat preferensi pengguna.</li>
            <li>Menganalisis penggunaan situs.</li>
        </ul>
        <p>Anda dapat mengatur preferensi cookie melalui browser Anda.</p>

        <h3>7. Perubahan Kebijakan Privasi</h3>
        <p>Kebijakan Privasi ini dapat diubah dari waktu ke waktu. Perubahan akan diumumkan di situs kami, dan
            penggunaan berkelanjutan Anda atas layanan ini berarti Anda menyetujui perubahan tersebut.</p>

        <h3>8. Hubungi Kami</h3>
        <p>Jika Anda memiliki pertanyaan tentang Kebijakan Privasi ini, Anda dapat menghubungi kami melalui email di <a
                href="mailto:privacy@techtalk.com">privacy@techtalk.com</a>.</p>
